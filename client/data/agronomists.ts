export const getAgronomistsByLocation = (location: string) => {
  if (location === "all") return agronomists;
  return agronomists.filter(agronomist => 
    agronomist.location === location || agronomist.counties.includes(location)
  );
};

export const searchAgronomists = (searchTerm: string) => {
  return agronomists.filter(agronomist =>
    agronomist.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
    agronomist.specialization.some(spec => spec.toLowerCase().includes(searchTerm.toLowerCase())) ||
    agronomist.title.toLowerCase().includes(searchTerm.toLowerCase()) ||
    agronomist.location.toLowerCase().includes(searchTerm.toLowerCase())
  );
};

export const getAvailableAgronomists = () => {
  return agronomists.filter(agronomist => agronomist.isAvailable);
};

export const getTopRatedAgronomists = (limit: number = 5) => {
  return [...agronomists].sort((a, b) => b.rating - a.rating).slice(0, limit);
};
